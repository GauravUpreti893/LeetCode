class Solution{
public: 
    long long int largestPrimeFactor(int N){
        // code here
        int num = 2;
        while (num * num <= N)
        {
            while (N % num == 0)
            N /= num;
            num++;
        }
        if (N == 1)
        return num - 1;
        return N;
    }
};

