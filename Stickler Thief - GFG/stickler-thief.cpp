class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        vector<int> dp(2, 0);
        for (int idx = n - 1; idx >= 0; idx--)
        {
            dp[idx&1] = max(dp[idx&1] + arr[idx], dp[!(idx&1)]);
        }
        return dp[0];
    }
};

