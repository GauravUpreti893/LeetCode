class Solution{   
public:
    int waysToBreakNumber(int N){
        // code here 
        long long ans = 0, x = N + 2, mod = 1e9 + 7;
        ans = (x)*(x - 1);
        ans %= mod;
        ans /= 2;
        ans %= mod;
        return ans;
    }
};

